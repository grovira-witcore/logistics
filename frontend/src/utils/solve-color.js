export function solveColor(colorName) {
  switch (colorName) {
    case 'red': {
      return '#FF4D4D';
    }
    case 'salmon': {
      return '#FA8072';
    }
    case 'orange': {
      return '#FFA500';
    }
    case 'yellow': {
      return '#FFD700';
    }
    case 'green': {
      return '#28A745';
    }
    case 'olive': {
      return '#808000';
    }
    case 'turquoise': {
      return '#40E0D0';
    }
    case 'teal': {
      return '#008080';
    }
    case 'sky-blue': {
      return '#87CEEB';
    }
    case 'blue': {
      return '#007BFF';
    }
    case 'navy-blue': {
      return '#000080';
    }
    case 'indigo': {
      return '#4B0082';
    }
    case 'purple': {
      return '#800080';
    }
    case 'magenta': {
      return '#FF00FF';
    }
    case 'pink': {
      return '#FF69B4';
    }
    case 'brown': {
      return '#8B4513';
    }
    case 'gray': {
      return '#A0A0A0';
    }
    case 'dark-gray': {
      return '#505050';
    }
    default: {
      return '#000000';
    }
  }
}
